public class Animal {
   String type;
   String category;
   String squad;
   String family;
   String genus;
   String view;

   public Animal() {
   }
   
   public Animal(String type, String category,String squad,
         String family, String genus, String view) {
      this.type = type;
      this.category = category;
      this.squad = squad;
      this.family = family;
      this.genus = genus;
      this.view = view;
         }

}
